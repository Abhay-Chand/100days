# LIST 
# SYNTAX:
'''
names = ["Jack", "Jill", "Harvey", "Bill", "Joseph"]
'''

# Using list constructor
my_list1 = list((1, 2, 3))
print(my_list1)


# Using square brackets[]
my_list2 = [1, 2, 3]
print(my_list2)

# with heterogeneous items
my_list3 = [1.0, 'Jessa', 3]
print(my_list3)

# empty list using list()
my_list4 = list()
print(my_list4)

# empty list using []
my_list5 = []
print(my_list4)